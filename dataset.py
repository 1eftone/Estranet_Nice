class ASCADv2Dataset(Dataset):
    def __init__(self, file_path, split='train', input_len=15000):
        self.file_path = file_path
        self.input_len = input_len
        if split == 'train':
            self.group_name = 'Profiling_traces'
        else:
            self.group_name = 'Attack_traces'
            
        print(f"Loading METADATA ONLY for {split} from {file_path}...")
        # 1. 这里我们只读取 Metadata (Key, Plaintext)，这些数据很小，不会 OOM
        with h5py.File(file_path, 'r') as f:
            self.dataset_len = f[self.group_name]['metadata'].shape[0]
            self.metadata = f[self.group_name]['metadata'][:]
            
            # ⚠️ 注意：这里绝对不读 'traces'！
            
        # 生成 Label
        plaintexts = self.metadata['plaintext'][:, 0]
        keys = self.metadata['key'][:, 0]
        self.labels = SBOX[plaintexts ^ keys]
        self.labels = torch.tensor(self.labels, dtype=torch.long)
        
        print(f"Dataset {split} initialized. Size: {self.dataset_len} (Lazy Loading)")

    def __len__(self):
        return self.dataset_len

    def __getitem__(self, idx):
        # 2. 只有在训练用到这一条数据时，才去硬盘读取
        # 这种方式对内存极度友好，内存占用几乎为 0
        with h5py.File(self.file_path, 'r') as f:
            # 只读这一条波形
            trace = f[self.group_name]['traces'][idx]
        
        # 截断
        if len(trace) > self.input_len:
            trace = trace[:self.input_len]
            
        # 转 float32
        trace = trace.astype(np.float32)
        
        # 实时归一化 (On-the-fly Normalization)
        # 之前的代码是 axis=1 (Trace-wise)，所以我们针对这一条波形求均值方差即可
        mean = np.mean(trace)
        std = np.std(trace)
        if std < 1e-10: std = 1.0
        trace = (trace - mean) / std
        
        # 转 Tensor (1, L)
        trace_tensor = torch.from_numpy(trace).unsqueeze(0)
        
        return trace_tensor, self.labels[idx]